const DICT: Record<Lang, Dict> = { fr: FR, en: EN, ar: AR };

type Ctx = { lang: Lang; setLang: (l: Lang) => void; t: (k: string) => string };
const LangContext = createContext<Ctx>({ lang: "fr", setLang: () => {}, t: (k) => k });

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("fr");

  useEffect(() => {
    const stored = (typeof window !== "undefined" &&
      localStorage.getItem("nef-lang")) as Lang | null;
    if (stored) setLangState(stored);
  }, []);

  useEffect(() => {
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
      document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    }
  }, [lang]);

  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("nef-lang", l);
    if (typeof window !== "undefined") {
      const url = new URL(window.location.href);
      url.searchParams.set("lang", l);
      window.history.replaceState({}, "", url);
    }
  };

  const t = (k: string) => getTranslation(lang, k);
  return <LangContext.Provider value={{ lang, setLang, t }}>{children}</LangContext.Provider>;
}

export function getTranslation(lang: Lang, key: string) {
  return DICT[lang]?.[key] ?? DICT.fr[key] ?? key;
}

export const useI18n = () => useContext(LangContext);

export function LangSwitcher() {
  const { lang, setLang } = useI18n();
  const langs: Lang[] = ["fr", "en", "ar"];
  return (
    <div className="flex gap-2 text-[10px] font-mono">
      {langs.map((l) => (
        <button
          key={l}
          onClick={() => setLang(l)}
          className={
            lang === l
              ? "text-charbon font-bold"
              : "text-charbon/40 hover:text-charbon transition-colors"
          }
        >
          {l.toUpperCase()}
        </button>
      ))}
    </div>
  );
}
